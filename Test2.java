public class Test2 {

	public static void main(String[] args) {
		double value;	double pi=3.14;
		value = 10+8;	System.out.println(value);
		value = 10*pi;	System.out.println(value);
		value = 314/pi;	System.out.println(value);
		value = pi-3;	System.out.println(value);
		value = (double)10/3;	System.out.println(value);
		value = 10%3;	System.out.println(value);
		value = 123%3;	//나머지값은 어떤 수를 나누건 0,1,2 중 하나
		System.out.println(value);
	}
}